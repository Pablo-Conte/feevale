package algorithms;

class DifferenceBetweenTwoPersons {
    public static void main(String[] args) {
        int agePersonOne = 25;
        int agePersonTwo = 30;

        int difference = agePersonTwo - agePersonOne;

        System.out.printf(("The difference between the ages of Person One and Person Two is %d years."), difference);
    }
}